    require_once('php/connection.php');
  
    // Query para trazer relatorio de servicos
    $result = $conn->prepare("
      SELECT cliente.nomeCliente, cliente.cpfCliente, pacote.tipoPacote, pacote.produtoPacote, pacote.valorPacote, servico.data_servico, (SELECT COUNT(*) FROM servico WHERE fk_Cliente = cliente.cpfCliente) qtd_servicos
      FROM servico 
      JOIN cliente ON cliente.cpfCliente = servico.fk_Cliente
      JOIN pacote  ON pacote.idPacote   = servico.fk_Pacote
      ORDER BY idServico DESC
    ");
    $result->execute();
    $rows = $result->fetchAll(PDO::FETCH_ASSOC);
